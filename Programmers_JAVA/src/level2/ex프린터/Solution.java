import java.util.Arrays;
import java.util.Collections;

class Solution {
	public int solution(int[] priorities, int location) {

		int answer = 0;

		Integer[] desc = new Integer[priorities.length];

		for (int i = 0; i < desc.length; i++) {
			desc[i] = priorities[i];
		}

		Arrays.sort(desc, Collections.reverseOrder());

		int i = 0;
		int j = 0;

		while (true) {
			if (desc[i] == priorities[j]) {
//				System.out.println("desc[" + i + "] == priorities[" + j + "]");
				answer++;
				i++;
				priorities[j] = 0;
				if (j == location)
					return answer;
			}
			j++;
			if (j == priorities.length)
				j = 0;
		}
	}
}