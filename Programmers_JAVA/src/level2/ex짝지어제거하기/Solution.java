import java.util.Stack;

class Solution{
    public int solution(String s){

        char[] arr = s.toCharArray();
        Stack<Character> stack = new Stack<>();
        
        for(char ch : arr){
            if(stack.empty())
                stack.push(ch);
            else if(stack.peek() != ch)
                stack.push(ch);
            else
                stack.pop();
        }
        
        return (stack.empty())? 1:0;
    }
}